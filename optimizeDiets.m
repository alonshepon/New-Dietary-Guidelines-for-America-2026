%   men: 90.6 kg, 175 cm
% women: 77.5 kg, 161 cm 
meanWeight = .49*90.6 + .51*77.5; % mean American body mass

p   = .8:.1:1.6; % protein intake, gProt/person/day/kgBodyWeight
Np  = length(p);
Xm  = zeros(Ng,Np);
