z   = zeros(Ng,1);
for i = 1:Np
  yp = [ 2500 meanWeight*p(i) ]' - A*MAD;
  X  = zeros(Ng,100);
  j  = 0;
  while j<100
    lb =  5 +  3*rand(Ng,1);
    ub = 85 + 15*rand(Ng,1);
    [x,~,eflag,out] = quadprog(H,z,[],[],A,yp,lb,ub,[],o);
    if eflag==1; j = j+1; X(:,j) = x + MAD; disp( [i j]);end
  end
  Xm(:,i) = mean(X   ,2);
  Xs(:,i) =  std(X,[],2);
end

